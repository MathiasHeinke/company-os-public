  const seq = formatPlaneWorkItemSequence(item);
  const sources = Array.isArray(contractFields.source_of_truth)
    ? contractFields.source_of_truth.map((p) => `- ${p}`).join("\n")
    : `- ${contractFields.source_of_truth || ""}`;

  const acceptance = Array.isArray(contractFields.acceptance_criteria)
    ? contractFields.acceptance_criteria.map((p) => `- ${p}`).join("\n")
    : `- ${contractFields.acceptance_criteria || ""}`;

  const gates = Array.isArray(contractFields.gates)
    ? contractFields.gates.map((p) => `- ${p}`).join("\n")
    : `- ${contractFields.gates || ""}`;

