#!/usr/bin/env node

/*
 * plane-dispatcher-v0.mjs
 *
 * v0 dispatcher: read + validate + lock-comment.
 *
 * Hard guarantees:
 *   - never transitions a work item state
 *   - never marks anything Done
 *   - never spawns a worker
 *   - never writes to Linear
 *   - never sends webhooks
 *   - default mode is dry-run (no Plane writes at all)
 *
 * --mode dry-run  : default. Reads work items, validates, prints what would happen.
 * --mode lock     : on accepted items, writes one worker.lock + one worker.context comment.
 *                   Still no state transitions, still no spawn.
 *
 * Source of truth:
 *   docs/orchestration/plane-worker-dispatcher-v0.md
 *   docs/orchestration/plane-role-routing.md
 *   docs/templates/worker-issue-contract.md
 */

