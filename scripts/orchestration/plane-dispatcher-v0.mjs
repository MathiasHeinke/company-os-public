function validateArgs(args) {
  const errors = [];
  if (!args.workspace) errors.push("--workspace is required");
  if (!args.projectId) errors.push("--project-id is required");
  if (!args.workItemId && !args.sequenceId) errors.push("--work-item-id or --sequence is required");
  if (!["dry-run", "lock"].includes(args.mode)) errors.push("--mode must be dry-run or lock");
  if (!["require", "warn", "off"].includes(args.contractReview)) errors.push("--contract-review must be require, warn, or off");
  return errors;
}

async function requestJson({ baseUrl, authHeaders, path, method = "GET", body }) {
  const response = await fetch(`${baseUrl}${path}`, {
    method,
    headers: {
      ...authHeaders,
      "Accept": "application/json",
      "Content-Type": "application/json",
    },
    body: body ? JSON.stringify(body) : undefined,
  });
  const text = await response.text();
  let parsed = null;
  try { parsed = text ? JSON.parse(text) : null; } catch { parsed = { raw: text.slice(0, 500) }; }
  return { ok: response.ok, status: response.status, body: parsed };
}

async function fetchWorkItem({ baseUrl, authHeaders, workspace, projectId, workItemId, sequenceId }) {
  if (workItemId) {
    const path = `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/work-items/${encodeURIComponent(workItemId)}/`;
    return requestJson({ baseUrl, authHeaders, path });
  }
  // Fallback: list and match by sequence_id (Phase 1; not paginated for performance, project assumed small).
  const path = `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/work-items/`;
  const listed = await requestJson({ baseUrl, authHeaders, path });
  if (!listed.ok) return listed;
  const rows = Array.isArray(listed.body) ? listed.body : (listed.body?.results || []);
  const wantedSeq = String(sequenceId).replace(/^.+-/, "");
  const found = rows.find((row) => String(row.sequence_id) === String(wantedSeq));
  if (!found) {
    return { ok: false, status: 404, body: { detail: `sequence ${sequenceId} not found in project` } };
  }

  // The list endpoint may return label IDs only. Re-fetch the matched item via
  // the detail endpoint so the dispatcher gets the richer embedded label
  // objects that include `name` under both api-key and app-token auth.
  const detailPath = `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/work-items/${encodeURIComponent(found.id)}/`;
  return requestJson({ baseUrl, authHeaders, path: detailPath });
}

/**
 * Resolve a work item's labels to role:* names.
 *
 * Order of precedence:
 *   1. **Embedded.** The Plane `/work-items/{id}/` endpoint returns the
 *      `labels` field as full label objects (id + name + meta) for both
 *      api-key and app-token. When that shape is detected, the dispatcher
 *      reads `name` directly. This is the canonical Phase 1 path — no
 *      label-management scope required.
 *   2. **Label-map fallback.** If a Plane endpoint ever returns only UUID
 *      strings (e.g. some list endpoints, or future API changes), the
 *      dispatcher tries `/labels/` first. If /labels/ also returns
 *      not-ok (esp. HTTP 403 with the Phase 1 App bot token), it falls
 *      back to the local label-map JSON generated by
 *      `plane-role-labels-bootstrap --write-label-map`.
 *
 * Returns:
 *   { ok: true, names: [...], source: "embedded" | "api" | "label-map", labelIds }
 *   { ok: false, reason: "<reject-code>", source: "...", labelIds }
 */
function sha256(text) {
  return createHash("sha256").update(text || "", "utf8").digest("hex");
}

function buildLockYaml({ item, role, parentSeat, contractFields, runId, expiresAt, supersededRunId }) {
  const sequence = formatPlaneWorkItemSequence(item);
  const lock = {
    "worker.lock": {
      version: "dispatcher-v0",
      parent_id: item.id,
      parent_sequence: sequence,
      role,
      parent_seat: parentSeat || "none",
      agent: contractFields.agent || null,
      mode: contractFields.mode || null,
      workspace: contractFields.workspace || "companyos",
      dispatch: contractFields.dispatch || "ready",
      human_gate: {
        level: contractFields["human_gate.level"] || contractFields.human_gate || "HG-2",
        owner: contractFields["human_gate.owner"] || "CEO",
      },
      acceptance_summary: Array.isArray(contractFields.acceptance_criteria)
        ? (contractFields.acceptance_criteria[0] || "")
        : (contractFields.acceptance_criteria || ""),
      expires_at: expiresAt,
      dispatcher_run_id: runId,
      superseded: supersededRunId || "none",
      hash: {
        description: canonicalDescriptionHash(item),
        labels: sha256(JSON.stringify((item._resolved_labels || []).slice().sort())),
      },
    },
  };
  return yamlBlock(lock);
}

function yamlBlock(obj, indent = 0) {
  const pad = (n) => " ".repeat(n);
  const lines = [];
  const emit = (key, value, depth) => {
    if (value && typeof value === "object" && !Array.isArray(value)) {
      lines.push(`${pad(depth)}${key}:`);
      for (const [k, v] of Object.entries(value)) emit(k, v, depth + 2);
    } else if (Array.isArray(value)) {
      lines.push(`${pad(depth)}${key}:`);
      for (const v of value) lines.push(`${pad(depth + 2)}- ${v}`);
    } else {
      lines.push(`${pad(depth)}${key}: ${value === null || value === undefined ? "null" : value}`);
    }
  };
  for (const [key, value] of Object.entries(obj)) emit(key, value, indent);
  return lines.join("\n");
}

