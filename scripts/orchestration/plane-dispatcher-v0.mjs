async function postComment({ baseUrl, authHeaders, workspace, projectId, workItemId, html }) {
  const path = `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/work-items/${encodeURIComponent(workItemId)}/comments/`;
  return requestJson({ baseUrl, authHeaders, path, method: "POST", body: { comment_html: html } });
}

async function listComments({ baseUrl, authHeaders, workspace, projectId, workItemId }) {
  const path = `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/work-items/${encodeURIComponent(workItemId)}/comments/`;
  const response = await requestJson({ baseUrl, authHeaders, path });
  if (!response.ok) return { ok: false, status: response.status, body: response.body, comments: [] };
  const rows = Array.isArray(response.body) ? response.body : (response.body?.results || []);
  return { ok: true, status: response.status, comments: rows };
}

async function fetchProjectIdentifier({ baseUrl, authHeaders, workspace, projectId }) {
  const response = await requestJson({
    baseUrl,
    authHeaders,
    path: `/api/v1/workspaces/${encodeURIComponent(workspace)}/projects/${encodeURIComponent(projectId)}/`,
  });
  if (!response.ok) return "";
  return String(response.body?.identifier || "").trim().toUpperCase();
}

/**
 * Scan comments for a prior worker.lock comment that is still active.
 * Returns { active: <lock-info|null>, superseded: <prev_run_id|null> }.
 *
 * "Active" means the comment was authored by us (title match) and its
 * embedded `expires_at` is in the future.
 *
 * "Superseded" carries the most recent expired run id so a new lock can
 * link back to the prior run for traceability.
 */
export function findExistingLock(comments, { currentDescriptionHash = "" } = {}) {
  const now = Date.now();
  let active = null;
  let supersededRunId = null;
  let supersededAt = -Infinity;
  let staleActive = null;

  for (const row of comments || []) {
    const html = row.comment_html || row.comment_stripped || "";
    if (!html.includes(LOCK_COMMENT_TITLE)) continue;
    const body = stripHtml(html);
    const yaml = body.match(/worker\.lock:\s*\n([\s\S]*?)(?:\n\S|$)/);
    if (!yaml) continue;
    const fields = parseYamlScalar(yaml[1]);
    const expiresAt = fields.expires_at ? Date.parse(fields.expires_at) : NaN;
    const runId = fields.dispatcher_run_id || null;
    const descriptionHash = extractLockDescriptionHash(body);
    if (Number.isFinite(expiresAt) && expiresAt > now) {
      if (currentDescriptionHash && descriptionHash && descriptionHash !== currentDescriptionHash) {
        staleActive = {
          dispatcher_run_id: runId,
          expires_at: fields.expires_at,
          comment_id: row.id || null,
          description_hash: descriptionHash,
        };
        supersededRunId = runId;
        supersededAt = expiresAt;
        continue;
      }
      active = {
        dispatcher_run_id: runId,
        expires_at: fields.expires_at,
        comment_id: row.id || null,
        description_hash: descriptionHash || null,
      };
      break;
    }
    if (Number.isFinite(expiresAt) && expiresAt > supersededAt) {
      supersededAt = expiresAt;
      supersededRunId = runId;
    }
  }

  return { active, superseded: supersededRunId, stale_active: staleActive };
}

function wrapAsCommentHtml(title, body) {
  return `<p><strong>${htmlEscape(title)}</strong></p><pre><code>${htmlEscape(body)}</code></pre>`;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    console.log(usage());
    return;
  }

  const errors = validateArgs(args);
  const auth = resolvePlaneAuth(args.auth);
  if (!auth.ok) errors.push(auth.missingError);

  const result = {
    version: "plane-dispatcher-v0",
    baseUrl: args.baseUrl,
    authMode: auth.authMode,
    hasCredential: auth.ok,
    workspace: args.workspace || null,
    projectId: args.projectId || null,
    mode: args.mode,
    contractReviewMode: args.contractReview,
    work_item: null,
    role_labels: [],
    contract_fields_seen: [],
    verdict: null,
    actions: [],
    ok: errors.length === 0,
    errors,
  };

  if (errors.length) {
    printResult(result, args.json);
    process.exitCode = 2;
    return;
  }

  const fetched = await fetchWorkItem({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
    workItemId: args.workItemId,
    sequenceId: args.sequenceId,
  });

  if (!fetched.ok) {
    result.ok = false;
    result.error = fetched.body;
    result.status = fetched.status;
    printResult(result, args.json);
    process.exitCode = 1;
    return;
  }

  const item = fetched.body;
  const projectIdentifier = await fetchProjectIdentifier({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
  });
  if (projectIdentifier) item._project_identifier = projectIdentifier;
  const resolvedLabelMapPath = args.labelMap
    ? args.labelMap
    : defaultLabelMapPath({ workspace: args.workspace, projectId: args.projectId });

  const labelResolution = await resolvePlaneItemLabels({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
    item,
    labelMapPath: resolvedLabelMapPath,
  });

  const labelNames = labelResolution.ok ? labelResolution.names : [];
  item._resolved_labels = labelNames;

  result.work_item = {
    id: item.id,
    sequence_id: item.sequence_id ? formatPlaneWorkItemSequence(item) : null,
    name: item.name,
    state: item.state,
  };
  result.role_labels = labelNames.filter((name) => name.startsWith("role:"));
  result.label_resolution = {
    ok: labelResolution.ok,
    source: labelResolution.source,
    label_map_path: resolvedLabelMapPath,
    label_ids_on_item: labelResolution.labelIds,
    api_status: labelResolution.api_status || null,
    reason: labelResolution.reason || null,
  };

  // Fail-closed before validation: if the resolver could not produce names
  // and the item carries label IDs, surface the resolver reason as a reject
  // code instead of letting the validator emit a misleading
  // role.label.missing.
  if (!labelResolution.ok && (labelResolution.labelIds || []).length > 0) {
    result.ok = false;
    result.actions.push({
      action: "reject",
      reasons: [labelResolution.reason],
      details: {
        source: labelResolution.source,
        label_map_path: resolvedLabelMapPath,
        api_status: labelResolution.api_status || null,
      },
    });
    printResult(result, args.json);
    process.exitCode = 2;
    return;
  }

  // The validator extracts the contract block from the description and
  // returns reason codes if anything is malformed. We pass labels too.
  const description = item.description_html || item.description_stripped || item.description || "";
  const verdict = validateContract({
    description: stripHtml(description),
    labels: labelNames,
    parentRoleLabel: undefined,
  });

  result.verdict = verdict;
  result.contract_fields_seen = verdict.evidence?.fields || [];

  if (!verdict.ok) {
    result.ok = false;
    result.actions.push({ action: "reject", reasons: verdict.reason_codes });
    printResult(result, args.json);
    process.exitCode = 2;
    return;
  }

  const role = result.role_labels[0];
  // Re-run the validator with the parsed contract fields to access them
  const parsed = await import("./worker-ledger-validator.mjs").then((m) => m.extractContractBlock(stripHtml(description)));
  const contractFields = parsed.ok ? parsed.fields : {};

  // Lock dedupe — applies to both dry-run and lock mode so the operator sees
  // accurate previews. The dispatcher refuses to layer a second active lock.
  const existing = await listComments({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
    workItemId: item.id,
  });
  if (!existing.ok) {
    result.ok = false;
    result.error = existing.body;
    result.status = existing.status;
    printResult(result, args.json);
    process.exitCode = 1;
    return;
  }

  if (args.contractReview !== "off") {
    const reviewGate = findContractReviewGate({
      comments: existing.comments,
      currentDescriptionHash: canonicalDescriptionHash(item),
    });
    result.contract_review = {
      ok: reviewGate.ok,
      mode: args.contractReview,
      reason_codes: reviewGate.reason_codes,
      comment_id: reviewGate.review?.comment_id || null,
      verdict: reviewGate.review?.fields?.verdict || null,
      reviewed_hash: reviewGate.review?.fields?.description_hash || null,
      current_hash: canonicalDescriptionHash(item),
    };
    if (!reviewGate.ok && args.contractReview === "require") {
      result.ok = false;
      result.actions.push({
        action: "reject",
        reasons: reviewGate.reason_codes,
        details: result.contract_review,
      });
      printResult(result, args.json);
      process.exitCode = 2;
      return;
    }
  }
  const currentDescriptionHash = canonicalDescriptionHash(item);
  const lockState = findExistingLock(existing.comments, { currentDescriptionHash });
  result.lock_state = {
    active_lock_present: Boolean(lockState.active),
    active_dispatcher_run_id: lockState.active?.dispatcher_run_id || null,
    superseded_run_id: lockState.superseded || null,
    stale_active_run_id: lockState.stale_active?.dispatcher_run_id || null,
  };

  if (lockState.active) {
    result.ok = false;
    result.actions.push({
      action: "reject",
      reasons: ["lock.duplicate"],
      details: { active_dispatcher_run_id: lockState.active.dispatcher_run_id, expires_at: lockState.active.expires_at },
    });
    printResult(result, args.json);
    process.exitCode = 2;
    return;
  }

  const runId = randomUUID();
  const expiresAt = new Date(Date.now() + LOCK_TTL_MIN * 60_000).toISOString();
  const lockYaml = buildLockYaml({
    item,
    role,
    parentSeat: contractFields.parent_seat,
    contractFields,
    runId,
    expiresAt,
    supersededRunId: lockState.superseded,
  });
  const contextText = buildContextComment({ item, role, contractFields, runId });

  if (args.mode === "dry-run") {
    result.actions.push({ action: "would-write-comment", comment_type: "worker.lock", preview: lockYaml });
    result.actions.push({ action: "would-write-comment", comment_type: "worker.context", preview: contextText });
    printResult(result, args.json);
    process.exitCode = 0;
    return;
  }

  // mode === "lock"
  const lockResp = await postComment({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
    workItemId: item.id,
    html: wrapAsCommentHtml(LOCK_COMMENT_TITLE, lockYaml),
  });
  result.actions.push({ action: "wrote-comment", comment_type: "worker.lock", ok: lockResp.ok, status: lockResp.status });

  const contextResp = await postComment({
    baseUrl: args.baseUrl,
    authHeaders: auth.headers,
    workspace: args.workspace,
    projectId: args.projectId,
    workItemId: item.id,
    html: wrapAsCommentHtml("worker.context (dispatcher-v0)", contextText),
  });
  result.actions.push({ action: "wrote-comment", comment_type: "worker.context", ok: contextResp.ok, status: contextResp.status });

  if (!lockResp.ok || !contextResp.ok) result.ok = false;
  printResult(result, args.json);
  if (!result.ok) process.exitCode = 1;
}

function printResult(result, json) {
  if (json) {
    console.log(JSON.stringify(result, null, 2));
    return;
  }

  console.log(`Plane dispatcher v0: ${result.ok ? "pass" : "fail"}`);
  console.log(`mode: ${result.mode}`);
  console.log(`auth: ${result.authMode}`);
  if (result.work_item) {
    console.log(`work item: ${result.work_item.sequence_id || result.work_item.id} ${result.work_item.name || ""}`);
  }
  console.log(`role labels: ${(result.role_labels || []).join(", ") || "(none)"}`);
  if (result.label_resolution) {
    const lr = result.label_resolution;
    console.log(`label resolution: source=${lr.source} ok=${lr.ok}${lr.api_status ? ` api_status=${lr.api_status}` : ""}${lr.reason ? ` reason=${lr.reason}` : ""}`);
    if (lr.label_map_path) console.log(`label map path: ${lr.label_map_path}`);
  }
  if (result.lock_state) {
    console.log(`active lock present: ${result.lock_state.active_lock_present}`);
    if (result.lock_state.superseded_run_id) {
      console.log(`superseded run id: ${result.lock_state.superseded_run_id}`);
    }
  }
  if (result.contract_review) {
    console.log(`contract review: mode=${result.contract_review.mode} ok=${result.contract_review.ok}${result.contract_review.verdict ? ` verdict=${result.contract_review.verdict}` : ""}`);
    for (const code of result.contract_review.reason_codes || []) console.log(`contract review reject: ${code}`);
  }
  if (result.verdict) {
    console.log(`verdict: ${result.verdict.ok ? "PASS" : "REJECT"}`);
    for (const code of result.verdict.reason_codes || []) console.log(`reject: ${code}`);
  }
  for (const action of result.actions || []) {
    if (action.action === "would-write-comment") {
      console.log(`would write comment: ${action.comment_type}`);
    } else if (action.action === "wrote-comment") {
      console.log(`wrote comment: ${action.comment_type} (HTTP ${action.status})`);
    } else if (action.action === "reject") {
      console.log(`rejected: ${action.reasons.join(", ")}`);
    }
  }
  for (const error of result.errors || []) console.log(`error: ${error}`);
  if (result.error) console.log(`error: ${JSON.stringify(result.error).slice(0, 300)}`);
}

const isMain = import.meta.url === `file://${process.argv[1]}`;
if (isMain) {
  main().catch((error) => {
    console.error(`Plane dispatcher v0 failed: ${error.message}`);
    process.exitCode = 1;
  });
}
